package com.nbcb.myron.bsen.module;

public class StoreInfo {
    private String id;
    private String name;
    private String age;
    private String goodsdesc;
    private String responsespeed;
    private String deliveryspeed;
    private String headturn;
    private String storesdesc;

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getAge() {
        return age;
    }

    public void setAge(String age) {
        this.age = age;
    }

    public String getGoodsdesc() {
        return goodsdesc;
    }

    public void setGoodsdesc(String goodsdesc) {
        this.goodsdesc = goodsdesc;
    }

    public String getResponsespeed() {
        return responsespeed;
    }

    public void setResponsespeed(String responsespeed) {
        this.responsespeed = responsespeed;
    }

    public String getDeliveryspeed() {
        return deliveryspeed;
    }

    public void setDeliveryspeed(String deliveryspeed) {
        this.deliveryspeed = deliveryspeed;
    }

    public String getHeadturn() {
        return headturn;
    }

    public void setHeadturn(String headturn) {
        this.headturn = headturn;
    }

    public String getStoresdesc() {
        return storesdesc;
    }

    public void setStoresdesc(String storesdesc) {
        this.storesdesc = storesdesc;
    }
}
